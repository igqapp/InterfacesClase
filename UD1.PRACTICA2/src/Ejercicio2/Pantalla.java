    private void btnEjecutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEjecutarActionPerformed
        // TODO add your handling code here:
        
        if(rBnueva.isSelected()){
            String nombreL = txtLocalidad.getText();
            String nombreP =cBProvincias.getSelectedItem().toString();
            Provincia p = lista.getProvincia(nombreP);
            Localidad localidad = new Localidad(nombreL);
            p.getListaLocalidades().add(localidad);
            actualizarLocalidades();
        
        }
        else if(rBeliminar.isSelected()){
            //NO FUNCIONA
            String nombreL = cBLocalidades.getSelectedItem().toString();
            String nombreP =cBProvincias.getSelectedItem().toString();
            Provincia p = lista.getProvincia(nombreP);
            Localidad l = p.getLocalidad(nombreL);
            lista.removeLocalidad(p, l);
            actualizarLocalidades();
        }
        
        else{//MODIFICAR NO IMPLEMENTADO TODAVIA
        
        }
    
    }//GEN-LAST:event_btnEjecutarActionPerformed

