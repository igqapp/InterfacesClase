    public void actualizarLocalidades(){
        String nombreProvinicia = cBProvincias.getSelectedItem().toString();
        Provincia p = lista.getProvincia(nombreProvinicia);
        cBLocalidades.removeAllItems();
        for (int i=0;i<p.getListaLocalidades().size();i++){
            cBLocalidades.addItem(p.getListaLocalidades().get(i).getNombre());
        }
        
    
    
    }
